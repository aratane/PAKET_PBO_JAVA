package BAB_2.Pewarisan.dua;

public class HitungSatu {
    int z;
    public void penambahan (int x, int y) {
        z = x + y;
        System.out.println("Hasil pertambahan : "+z);
    }
    public void pengurangan (int x, int y) {
        z = x - y;
        System.out.println("Hasil pengurangan : "+z);
    }

}
